#crie um programa que leia dois numeros e exiba a soma entre eles
#int inteiro
n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor:'))
s= n1+n2
print('A soma entre {} e {} vale {}'.format(n1,n2,s))

#float
a1 = float(input('Digite um valor: '))
a2 = float(input('Digite outro valor:'))
a= a1+a2
print('A soma entre {} e {} vale {}'.format(a1,a2,a))

#bool
b1 = bool(input('Digite um valor: '))
b2 = bool(input('Digite outro valor:'))
b= b1+b2
print('A soma entre {} e {} vale {}'.format(b1,b2,b))

#str string
s1 = str(input('Digite um valor: '))
s2 = str(input('Digite outro valor:'))
ss= s1+s2
print('A soma entre {} e {} vale {}'.format(s1,s2,ss))